# 3.8: Seeing the world
places = ["russia", "australia", "netherlands", "japan", "brazil"]
print(places)
print("\nThis is the list in alphabetical order")
print(sorted(places))
print("\nThe original list")
print(places)

print("\nThis is the list reversed")
places.reverse()
print(places)
places.reverse()

print("\nThis is the list back to normal")
print(places)

print("\nThis is the list in alphabetical order")
places.sort()
print(places)

print("\nThis is the list reversed")
places.sort(reverse=True)
print(places)

